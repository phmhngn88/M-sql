SELECT MAGV 
FROM GIAOVIEN
WHERE HOTEN LIKE '%Nguyễn' AND luong > 2000
UNION
SELECT truongbm
FROM BOMON
WHERE (YEAR(BOMON.NGAYNHANCHUC)) >1995 AND (BOMON.truongbm IS NOT NULL)
GO

SELECT HOTEN
FROM GIAOVIEN, BOMON
WHERE (GIAOVIEN.mabm = BOMON.mabm) AND (BOMON.makhoa = 'CNTT')
GO


SELECT *
FROM GIAOVIEN, BOMON
WHERE (GIAOVIEN.magv = BOMON.truongbm) 
GO

SELECT GIAOVIEN.hoten, GIAOVIEN.magv, BOMON.*
FROM GIAOVIEN, BOMON
WHERE (GIAOVIEN.mabm = BOMON.MABM) 
GO

SELECT DETAI.tendt, GIAOVIEN.hoten
FROM DETAI, GIAOVIEN
WHERE DETAI.gvcndt = GIAOVIEN.magv
GO

SELECT KHOA.tenkhoa, GIAOVIEN.*
FROM KHOA, GIAOVIEN
WHERE KHOA.truongkhoa = GIAOVIEN.magv
GO

SELECT GIAOVIEN.*
FROM BOMON, GIAOVIEN, THAMGIADT
WHERE GIAOVIEN.mabm = BOMON.mabm AND BOMON.tenbm = 'Vi sinh' AND GIAOVIEN.magv = THAMGIADT.magv AND THAMGIADT.madt = '006'
GO

SELECT DETAI.madt, CHUDE.TENCD, GIAOVIEN.hoten, GIAOVIEN.ngsinh, GIAOVIEN.diachi
FROM DETAI, CHUDE, GIAOVIEN
WHERE DETAI.capql = N'Nhà Nước' AND CHUDE.macd = DETAI.macd AND GIAOVIEN.magv = DETAI.gvcndt
GO


